def _six_dof_axis_rna_fields() -> tuple[dict, ...]:
    """生成 SixDOF 六轴属性，保持字段命名与 native 轴序一致。"""
    definitions = (
        ("translation_x", "平移X", "LENGTH", -1.0, 1.0),
        ("translation_y", "平移Y", "LENGTH", -1.0, 1.0),
        ("translation_z", "平移Z", "LENGTH", -1.0, 1.0),
        ("rotation_x", "旋转X", "ROTATION", -0.7853981633974483, 0.7853981633974483),
        ("rotation_y", "旋转Y", "ROTATION", -0.7853981633974483, 0.7853981633974483),
        ("rotation_z", "旋转Z", "ROTATION", -0.7853981633974483, 0.7853981633974483),
    )
    fields = []
    for axis_name, label, unit, default_min, default_max in definitions:
        fields.append({
            "name": f"six_dof_{axis_name}_mode",
            "property": "enum",
            "kwargs": {
                "name": f"{label}模式",
                "description": f"SixDOF {label}轴是自由、固定到零位还是限制在范围内",
                "items": [
                    ("FREE", "自由", "该轴不施加约束"),
                    ("FIXED", "固定", "该轴固定在约束frame零位"),
                    ("LIMITED", "限制", "该轴限制在最小值和最大值之间"),
                ],
                "default": "FIXED",
            },
        })
        for suffix, suffix_label, default in (
            ("min", "最小", default_min), ("max", "最大", default_max),
        ):
            kwargs = {
                "name": f"{label}{suffix_label}",
                "description": f"SixDOF {label}轴限制的{suffix_label}值",
                "default": default,
                "unit": unit,
            }
            if unit == "ROTATION":
                kwargs.update({"min": -3.141592653589793, "max": 3.141592653589793})
            fields.append({
                "name": f"six_dof_{axis_name}_{suffix}",
                "property": "float",
                "kwargs": kwargs,
            })
        if axis_name.startswith("translation"):
            fields.extend((
                {
                    "name": f"six_dof_{axis_name}_limit_spring_frequency",
                    "property": "float",
                    "kwargs": {
                        "name": f"{label}限制弹簧频率",
                        "description": f"SixDOF {label}软限制的频率；0表示硬限制",
                        "default": 0.0,
                        "min": 0.0,
                        "soft_max": 20.0,
                    },
                },
                {
                    "name": f"six_dof_{axis_name}_limit_spring_damping",
                    "property": "float",
                    "kwargs": {
                        "name": f"{label}限制弹簧阻尼",
                        "description": f"SixDOF {label}软限制的阻尼比",
                        "default": 0.0,
                        "min": 0.0,
                        "soft_max": 2.0,
                    },
                },
            ))
        friction_kind = "摩擦力" if axis_name.startswith("translation") else "摩擦力矩"
        fields.append({
            "name": f"six_dof_{axis_name}_friction",
            "property": "float",
            "kwargs": {
                "name": f"{label}{friction_kind}",
                "description": f"SixDOF {label}轴的最大{friction_kind}；0表示无摩擦",
                "default": 0.0,
                "min": 0.0,
                "soft_max": 1000.0,
            },
        })
        fields.append({
            "name": f"six_dof_{axis_name}_motor_state",
            "property": "enum",
            "kwargs": {
                "name": f"{label} Motor",
                "description": f"SixDOF {label}轴的 Motor 驱动模式",
                "items": [
                    ("OFF", "关闭", "不驱动该轴"),
                    ("VELOCITY", "速度", "驱动该轴达到目标速度"),
                    ("POSITION", "位置", "驱动该轴达到目标位置或姿态"),
                ],
                "default": "OFF",
            },
        })
    return tuple(fields)


